  Ingredient("sugar", 1, INGREDIENT_CAT.OTHER),
  Ingredient("milk", 0.5, INGREDIENT_CAT.OTHER),
  Ingredient("vanilla-extract", 0.5, INGREDIENT_CAT.OTHER),
  Ingredient("nutmeg", 1, INGREDIENT_CAT.OTHER),
  Ingredient("cinnamon", 1, INGREDIENT_CAT.OTHER),
